// Declare two numbers
let a = 2;
let b = 3;

// Arithmetic operations
let addition = a + b;
let subtraction = a - b;
let multiplication = a * b;
let division = a / b;
let modulus = a % b;
let exponent = a ** b;

// Display results
console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);
console.log("Modulus:", modulus);
console.log("Exponentiation:", exponent);


let x = 5;

console.log("Initial value of x:", x);

// Post-increment
console.log("Post-increment (x++):", x++);
console.log("Value after post-increment:", x);

// Pre-increment
console.log("Pre-increment (++x):", ++x);
console.log("Value after pre-increment:", x);

// Post-decrement
console.log("Post-decrement (x--):", x--);
console.log("Value after post-decrement:", x);

// Pre-decrement
console.log("Pre-decrement (--x):", --x);
console.log("Value after pre-decrement:", x);
