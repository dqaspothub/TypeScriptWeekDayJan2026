let x = 10;

console.log("Initial value:", x);

// Assignment
x = 20;
console.log("After = :", x);

// Add and assign
x += 5;   // x = x + 5
console.log("After += :", x);

// Subtract and assign
x -= 3;   // x = x - 3
console.log("After -= :", x);

// Multiply and assign
x *= 2;   // x = x * 2
console.log("After *= :", x);

// Divide and assign
x /= 4;   // x = x / 4
console.log("After /= :", x);

// Modulus and assign
x %= 3;   // x = x % 3
console.log("After %= :", x);
